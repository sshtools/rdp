package com.sshtools.javardp;

public class RdesktopCryptoException extends RdesktopException {
	private static final long serialVersionUID = -6250374432272784769L;

	public RdesktopCryptoException() {
		super();
	}

	public RdesktopCryptoException(String message, Throwable cause) {
		super(message, cause);
	}

	public RdesktopCryptoException(String message) {
		super(message);
	}

	public RdesktopCryptoException(Throwable cause) {
		super(cause);
	}
}
